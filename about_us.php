<h1>Historia</h1>
<br>

<div class="aboutus">
	Mer än en miljon människor har åkt i något av Skidloppet ABs lopp sedan starten 1956.
	Men egentligen föddes tävlingen ytterligare 100 år tidigare, med bara en enda person.
