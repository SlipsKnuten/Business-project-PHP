<h1>Nutid</h1>
<br>
<div class="aboutus">

Skidloppet AB anordnar tävlingar inom skidåkning och löpning varje år med start i Hedemora och målgång i Grängesberg. 
Tävlingarna har gått av stapeln varje år sedan 1965 och visionen hos Skidloppet AB är att utöka både antalet tävlingsformer och antalet deltagare årligen. 
Skidloppet ABs filosofi är att "alla ska med", vilket innebär att alla människor ska känna sig välkomna att deltaga i tävlingarna oavsett kapacitet, kön, hudfärg och sexuell läggning.
</div>
